md" ### Απόκριση συστημάτων $1^{ης}$ τάξης

Ένα σύστημα 1ης τάξης έχει την εξής μορφή:

$$\frac{Y(s)}{R(s)} = \frac{1}{Ts+1}$$

Εάν θεωρήσουμε είσοδο μοναδιάιας βαθμίδας τότε προκύπτει: 

$$Y(s) = \frac{1}{Ts+1}R(s) = \frac{1}{Ts+1}\frac{1}{s}$$

και με αντίστροφο μετασχηματισμός Laplace:

$$y(t) = 1-e^{-t/T}, t \geq 0$$

Ενώ για t = T, έχουμε:

$$y(t) = 1-e^{-1} = 0.632$$

"

