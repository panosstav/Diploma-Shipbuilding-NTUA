* Όταν ζ = 1, το σύστημα λέγεται ότι έχει κρίσιμη απόσβεση (critically damped). 

* Όταν ζ > 1, το σύστημα λέγεται ότι έχει υπερ-απόσβεση (overdamped) και η μεταβατική απόκριση δεν έχει ταλάντωση 

