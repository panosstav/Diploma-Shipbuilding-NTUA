$$s^2+2ζω_n+ω_n^2 = 0$$

Οι ρίζες της χαρακτηριστικής εξίσωσης ονομάζονται πόλοι του συστήματος. Αντίστοιχα, σε περίπτωση ύπαρξης ριζών στον αριθμητή, αυτές ονομάζονται μηδενιστές. Τα $ω_n$ και $ζ$ περιγράφουν τη δυναμική συμπεριφορά συστημάτων 2ης τάξης. 

* Όταν 0 < ζ < 1, οι πόλοι κλειστού βρόχου είναι μιγαδικοί συζυγείς, και βρίσκονται στο αριστερό ημιεπίπεδο s. Το σύστημα λέγεται ότι έχει υπο-απόσβεση (underdamped) και η μεταβατική απόκριση είναι μια αποσβυνόμενη ταλάντωση. 

